    public void mostrarDatos() {
        String[] titulos = {"Tipo", "Documento", "Nombre", "Genero", "Direccion", "Telefono"};
        String[] registros = new String[7];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select* from datos";

        try {
            java.sql.Statement st = con.createStatement();
            var rs = st.executeQuery(SQL);

            while (rs.next()) {
                registros[0] = rs.getString("cbTipodocumento");
                registros[1] = rs.getString("documento");
                registros[2] = rs.getString("nombre");
                registros[3] = rs.getString("genero");
                registros[4] = rs.getString("direccion");
                registros[5] = rs.getString("celular");
                modelo.addRow(registros);
            }
            tablaAprendiz.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de busqueda" + e.getMessage());
        }

    } 
    
    public void actualizarDatos(){
        try {
            String SQL = "update datos set cbTipodocumento=?,documento=?,nombre=?,genero=?,direccion=?,celular=? where documento=?";

            int filaSeleccionada = tablaAprendiz.getSelectedRow();
            String ref = (String) tablaAprendiz.getValueAt(filaSeleccionada, 1);

            java.sql.PreparedStatement pst = con.prepareStatement(SQL);
            int seleccionado = cbTipodocumento.getSelectedIndex();
            pst.setString(1, cbTipodocumento.getItemAt(seleccionado));

            pst.setDouble(2, Double.parseDouble(documento.getText()));
            pst.setString(3, nombre.getText());

            int seleccionado1 = genero.getSelectedIndex();
            pst.setString(4, genero.getItemAt(seleccionado1));

            pst.setString(5, direccion.getText());
            pst.setDouble(6, Double.parseDouble(celular.getText()));

            pst.setString(7, ref);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Datos Actualizados Correctamente");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de Actualizacion" + e.getMessage());

        }
    }
    public void eliminarRegistro(){
            int filaSeleccionada=tablaAprendiz.getSelectedRow();
           
            try{
                String SQL="delete from datos where documento="+tablaAprendiz.getValueAt(filaSeleccionada,1);
                
                java.sql.Statement st=con.createStatement();
                int n=st.executeUpdate(SQL);
                
                if(n>=0){
                    JOptionPane.showMessageDialog(null,"Registro Eliminado");
                }
            } catch(Exception e){
                JOptionPane.showMessageDialog(null,"Error al Eliminar Registro"+e.getMessage());
            }
        }
    public void filtrarDatos(String valor) {
        String[] titulos = {"Tipo", "Documento", "Nombre", "Genero", "Direccion", "Telefono"};
        String[] registros = new String[7];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select* from datos where nombre like'%"+valor+"%'";
        

        try {
            java.sql.Statement st = con.createStatement();
            var rs = st.executeQuery(SQL);

            while (rs.next()) {
                registros[0] = rs.getString("cbTipodocumento");
                registros[1] = rs.getString("documento");
                registros[2] = rs.getString("nombre");
                registros[3] = rs.getString("genero");
                registros[4] = rs.getString("direccion");
                registros[5] = rs.getString("celular");
                modelo.addRow(registros);
            }
            tablaAprendiz.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de Registro" + e.getMessage());
        }
        
    }
        
